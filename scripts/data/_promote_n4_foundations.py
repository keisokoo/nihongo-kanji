def main():
    seed_path = Path(__file__).parent / "grammar-n4.json"
    with seed_path.open(encoding="utf-8") as f:
        d = json.load(f)

    expected_rf = {
        22: "verb:volitional",
        73: "verb:causative-passive",
        74: "verb:causative",
        119: "verb:passive",
    }

    found = set()
    for it in d["items"]:
        if it["no"] in EXPLANATIONS:
            assert it.get("ruleFamily") == expected_rf[it["no"]], \
                f"no.{it['no']} ruleFamily mismatch: {it.get('ruleFamily')!r} != {expected_rf[it['no']]!r}"
            old_len = len(it.get("explanation", ""))
            it["explanation"] = EXPLANATIONS[it["no"]]
            it["isFoundation"] = True
            found.add(it["no"])
            print(f"✅ no.{it['no']:>3} {it['pattern']!r}: {old_len} → {len(it['explanation'])} chars + isFoundation=true")

    missing = set(EXPLANATIONS) - found
    if missing:
        print(f"❌ 누락: {sorted(missing)}")
        return

    with seed_path.open("w", encoding="utf-8") as f:
        json.dump(d, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"\n✅ N4: {len(found)} items promoted to foundation.")


if __name__ == "__main__":
    main()
