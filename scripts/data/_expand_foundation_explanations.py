def main():
    seed_path = Path(__file__).parent / "grammar-n5.json"
    with seed_path.open(encoding="utf-8") as f:
        d = json.load(f)

    target_nos = set(EXPLANATIONS.keys())
    found = set()
    for it in d["items"]:
        if it["no"] in target_nos:
            old = it.get("explanation", "")
            it["explanation"] = EXPLANATIONS[it["no"]]
            found.add(it["no"])
            print(f"✅ no.{it['no']:>3} {it['pattern']!r}: {len(old)} → {len(it['explanation'])} chars")

    missing = target_nos - found
    if missing:
        print(f"❌ 누락: {sorted(missing)}")
        return

    with seed_path.open("w", encoding="utf-8") as f:
        json.dump(d, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"\n✅ {len(found)} foundations updated.")


if __name__ == "__main__":
    main()
